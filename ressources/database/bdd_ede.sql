	"sexe"	varchar(255) DEFAULT NULL,
	"date_naiss"	date DEFAULT NULL,
	"cod_pays_naiss"	varchar(255) DEFAULT NULL,
	"num_exp_naiss"	varchar(255) DEFAULT NULL,
	"cod_pays_pere"	varchar(255) DEFAULT NULL,
	"num_nat_pere"	varchar(255) DEFAULT NULL,
	"cod_race_pere"	varchar(255) DEFAULT NULL,
	"cod_pays_mere"	varchar(255) DEFAULT NULL,
	"num_nat_mere"	varchar(255) DEFAULT NULL,
	"cod_race_mere"	varchar(255) DEFAULT NULL,
	"num_elevage"	varchar(255) DEFAULT NULL,
	"cod_race"	varchar(255) DEFAULT NULL,
	"date_modif"	DATE,
	"actif"	VARCHAR(1) DEFAULT '1',
	FOREIGN KEY("num_elevage") REFERENCES "elevage"("num_elevage"),
	FOREIGN KEY("cod_race") REFERENCES "race"("cod_race"),
	PRIMARY KEY("num_nat")
);
CREATE TABLE IF NOT EXISTS "soins" (
	"num_nat"	VARCHAR(255) NOT NULL,
	"nom_soin"	TEXT,
	"dose"	TEXT,
	"date_soin"	DATE,
	FOREIGN KEY("num_nat") REFERENCES "animal"("num_nat")
);
CREATE TABLE IF NOT EXISTS "vaccins" (
	"num_nat"	VARCHAR(255) NOT NULL,
	"nom_vaccin"	TEXT,
	"dose"	TEXT,
	"date_vaccin"	DATE,
	FOREIGN KEY("num_nat") REFERENCES "animal"("num_nat")
);
