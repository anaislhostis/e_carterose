package com.example.e_carterose;

public class Vaccins {
    private String num_nat;
    private String nom_vaccin;
    private String dose;
    private String date_vaccin;


    public String getNumNat() {
        return num_nat;
    }

    public void setNumNat(String num_nat) {
        this.num_nat = num_nat;
    }

    public String getNomVaccin() {
        return nom_vaccin;
    }

    public void setNomVaccin(String nom_vaccin) {
        this.nom_vaccin = nom_vaccin;
    }

    public String getDose() {
        return dose;
    }

    public void setDose(String dose) {
        this.dose = dose;
    }

    public String getDateVaccin() {
        return date_vaccin;
    }

    public void setDateVaccin(String date_vaccin) {
        this.date_vaccin = date_vaccin;
    }
}
