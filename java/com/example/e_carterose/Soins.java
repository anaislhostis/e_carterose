package com.example.e_carterose;

public class Soins {
    private String num_nat;
    private String nom_soin;
    private String dose;
    private String date_soin;


    public String getNumNat() {
        return num_nat;
    }

    public void setNumNat(String num_nat) {
        this.num_nat = num_nat;
    }

    public String getNomSoin() {
        return nom_soin;
    }

    public void setNomSoin(String nom_soin) {
        this.nom_soin = nom_soin;
    }

    public String getDose() {
        return dose;
    }

    public void setDose(String dose) {
        this.dose = dose;
    }

    public String getDateSoin() {
        return date_soin;
    }

    public void setDateSoin(String date_soin) {
        this.date_soin = date_soin;
    }
}
